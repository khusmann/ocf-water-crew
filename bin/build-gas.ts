// Builds the Apps Script deploy directory at dist/. tsc compiles
// src/ → build/; we concat the resulting .js files in dependency
// order, strip ES module syntax (GAS has no module system — all
// files share one global scope), and emit a single dist/scheduler.js
// plus the appsscript manifest.
//
// Bundling to one file (vs. emitting each as its own GAS file) keeps
// the build trivial: no name-prefix scheme for cross-file `const` TDZ,
// no collision-detection, no recursive walk. Order = src/ layering.
import fs from "node:fs";
import path from "node:path";
import { spawnSync } from "node:child_process";

const repoRoot = path.resolve(import.meta.dirname, "..");
const buildDir = path.join(repoRoot, "build");
const distDir = path.join(repoRoot, "dist");

const order = [
  "engine.js",
  "rules.js",
  "rulesets.js",
  "scheduler.js",
  "sheet.js",
];

fs.rmSync(buildDir, { recursive: true, force: true });
fs.rmSync(distDir, { recursive: true, force: true });
fs.mkdirSync(distDir, { recursive: true });

const tsc = spawnSync(
  "npx",
  ["tsc", "-p", path.join(repoRoot, "tsconfig.json")],
  { cwd: repoRoot, stdio: "inherit" }
);
if (tsc.status !== 0) process.exit(tsc.status ?? 1);

// Strip the two ESM constructs tsc emits into our src/ files:
//   - `import { ... } from "..."` lines (single-line in tsc's output)
//   - `export ` prefix on declarations
// `export {};` (tsc's type-only-file marker) becomes `{};` — a no-op
// empty block at runtime; not worth the regex to remove.
const strip = (body: string): string =>
  body
    .replace(/^import\b[^\n]*\n/gm, "")
    .replace(/^export\s+/gm, "");

const sections = order.map((name) => {
  const body = strip(fs.readFileSync(path.join(buildDir, name), "utf8")).trim();
  return `// ===== src/${name.replace(/\.js$/, ".ts")} =====\n${body}`;
});

const bundle =
  "// Generated by bin/build-gas.ts — do not edit. Regenerate with `npm run build`.\n\n" +
  sections.join("\n\n") +
  "\n";

fs.writeFileSync(path.join(distDir, "scheduler.js"), bundle);
fs.copyFileSync(
  path.join(repoRoot, "appsscript.json"),
  path.join(distDir, "appsscript.json")
);

console.log("Wrote:\n  dist/scheduler.js\n  dist/appsscript.json");
